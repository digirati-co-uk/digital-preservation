    [Fact]
    public async Task Raw_XML_In_Special_Chars_Fixture_Has_Encoded_Ampersand()
    {
        // Verifies that the ampersand in the fixture file's attribute values is
        // correctly entity-encoded as &amp; in the raw XML bytes, which ensures
        // the file is well-formed XML.

        var metsUri = CopyFixture("path-fixture-special.xml", "fixture-special-rawxml.xml");

        // Read as raw text to confirm entity encoding in the file on disk
        var rawXml = await File.ReadAllTextAsync(metsUri.LocalPath);
        rawXml.Should().Contain("AT&amp;T guide.pdf",
            "ampersand in attribute values must be entity-encoded in well-formed XML");
        rawXml.Should().NotContain("AT&T guide.pdf",
            "a raw unencoded ampersand would make the XML malformed");

        // XDocument decodes &amp; back to & so LINQ-to-XML sees the original string
        var doc = XDocument.Load(metsUri.LocalPath);
        doc.Descendants(MetsNs + "file")
            .Should().Contain(f => (string)f.Attribute("ID")! == "FILE_objects/AT&T guide.pdf");
        doc.Descendants(MetsNs + "amdSec")
            .Should().Contain(a => (string?)a.Attribute("ID") == "ADM_objects/AT&T guide.pdf");
    }
