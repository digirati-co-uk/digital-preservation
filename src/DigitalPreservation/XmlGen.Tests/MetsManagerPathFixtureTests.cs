    [Fact]
    public async Task MetsParser_Reads_Spaces_Fixture_Correctly()
    {
        // The parser must correctly populate LocalPath, Name, Digest, and Size
        // for every entry in the fixture, even though the METS ID attributes contain
        // spaces and are technically not valid XML NCNames.

        var metsUri = CopyFixture("path-fixture-spaces.xml", "fixture-spaces-parse.xml");
        var result = await parser.GetMetsFileWrapper(metsUri);

        result.Success.Should().BeTrue();
        var wrapper = result.Value!;

        // All four files present (including the METS file itself)
        var objFiles = wrapper.Files.Where(f => f.LocalPath.StartsWith("objects/")).ToList();
        objFiles.Should().HaveCount(3);

        var f1 = wrapper.Files.Single(f => f.LocalPath == "objects/my file.pdf");
        f1.Name.Should().Be("my file.pdf");
        f1.ContentType.Should().Be("application/pdf");
        f1.Digest.Should().Be(Digest1);
        f1.Size.Should().Be(54321);

        var f2 = wrapper.Files.Single(f => f.LocalPath == "objects/my great file.pdf");
        f2.Name.Should().Be("my great file.pdf");
        f2.Digest.Should().Be(Digest2);
        f2.Size.Should().Be(12345);

        var f3 = wrapper.Files.Single(f => f.LocalPath == "objects/my folder/my document.pdf");
        f3.Name.Should().Be("my document.pdf");
        f3.Digest.Should().Be(Digest3);
        f3.Size.Should().Be(99999);

        // Directory structure
        var phys = wrapper.PhysicalStructure!;
        var objDir = phys.Directories.Single(d => d.Name == FolderNames.Objects);
        objDir.Directories.Should().HaveCount(1);

        var myFolder = objDir.Directories.Single();
        myFolder.Name.Should().Be("my folder");
        myFolder.LocalPath.Should().Be("objects/my folder");
        myFolder.Files.Should().HaveCount(1);
        myFolder.Files[0].LocalPath.Should().Be("objects/my folder/my document.pdf");
    }

    [Fact]
    public async Task MetsParser_Reads_Premis_Metadata_From_Spaces_Fixture()
    {
        // MetsParser must populate FileFormatMetadata from PREMIS techMD for files
        // whose ADMID attribute contains spaces and was therefore split into multiple
        // tokens by the XmlSerializer. The parser must still map the split ADMID back
        // to the correct AmdSec element.
        //
        // The fixture files were created without FileFormatMetadata in WorkingFile.Metadata
        // (no pipeline run). MetsParser produces a fallback FileFormatMetadata with
        // PronomKey="UNKNOWN" and Digest from the PREMIS fixity block, but without Size
        // (Size is only populated when a PRONOM key is present). The top-level
        // WorkingFile.Size IS populated from premis:size.

        var metsUri = CopyFixture("path-fixture-spaces.xml", "fixture-spaces-premis.xml");
        var result = await parser.GetMetsFileWrapper(metsUri);
        result.Success.Should().BeTrue();

        // Parser always synthesises a FileFormatMetadata from PREMIS, even without PRONOM info
        var f1 = result.Value!.Files.Single(f => f.LocalPath == "objects/my file.pdf");
        var ffm1 = f1.Metadata.OfType<FileFormatMetadata>().Single();
        ffm1.Source.Should().Be("METS");
        ffm1.PronomKey.Should().Be("UNKNOWN");
        ffm1.Digest.Should().Be(Digest1);
        f1.Size.Should().Be(54321); // top-level, populated from premis:size

        var f2 = result.Value.Files.Single(f => f.LocalPath == "objects/my great file.pdf");
        var ffm2 = f2.Metadata.OfType<FileFormatMetadata>().Single();
        ffm2.Digest.Should().Be(Digest2);
        f2.Size.Should().Be(12345);

        var f3 = result.Value.Files.Single(f => f.LocalPath == "objects/my folder/my document.pdf");
        var ffm3 = f3.Metadata.OfType<FileFormatMetadata>().Single();
        ffm3.Digest.Should().Be(Digest3);
        f3.Size.Should().Be(99999);
    }

