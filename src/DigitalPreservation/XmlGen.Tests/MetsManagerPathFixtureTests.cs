    // Internal helpers used by the generator (assert-free — generator should fail loudly)
    private async Task<string> DoAddFile(Uri metsUri, WorkingFile file, string eTag)
    {
        var result = await metsManager.HandleSingleFileUpload(metsUri, file, eTag);
        result.Success.Should().BeTrue(result.ErrorMessage ?? "");
        var parsed = await parser.GetMetsFileWrapper(metsUri);
        return parsed.Value!.ETag!;
    }

    private async Task<string> DoAddDirectory(Uri metsUri, WorkingDirectory dir, string eTag)
    {
        var result = await metsManager.HandleCreateFolder(metsUri, dir, eTag);
        result.Success.Should().BeTrue(result.ErrorMessage ?? "");
        var parsed = await parser.GetMetsFileWrapper(metsUri);
        return parsed.Value!.ETag!;
    }

