    [Fact]
    public async Task Raw_XML_In_Spaces_Fixture_Has_Consistent_IDs()
    {
        // Verifies that the committed fixture file has consistent cross-references
        // between its ID, ADMID, FILEID, and AmdSec/TechMD ID attributes.
        // This is a static check — no MetsManager editing involved.

        var metsUri = CopyFixture("path-fixture-spaces.xml", "fixture-spaces-rawxml.xml");
        var doc = XDocument.Load(metsUri.LocalPath);

        // "my file.pdf": single space splits ADMID into two tokens
        var fileEl = doc.Descendants(MetsNs + "file")
            .Single(f => (string)f.Attribute("ID")! == "FILE_objects/my file.pdf");
        fileEl.Attribute("ADMID")!.Value.Should().Be("ADM_objects/my file.pdf");
        doc.Descendants(MetsNs + "amdSec")
            .Should().Contain(a => (string?)a.Attribute("ID") == "ADM_objects/my file.pdf");

        // "my great file.pdf": three spaces give four tokens
        var fileEl2 = doc.Descendants(MetsNs + "file")
            .Single(f => (string)f.Attribute("ID")! == "FILE_objects/my great file.pdf");
        fileEl2.Attribute("ADMID")!.Value.Should().Be("ADM_objects/my great file.pdf");
        doc.Descendants(MetsNs + "amdSec")
            .Should().Contain(a => (string?)a.Attribute("ID") == "ADM_objects/my great file.pdf");

        // "my folder" directory: Div ID and ADMID
        var dirDiv = doc.Descendants(MetsNs + "div")
            .Single(d => (string?)d.Attribute("ID") == "PHYS_objects/my folder");
        dirDiv.Attribute("ADMID")!.Value.Should().Be("ADM_objects/my folder");

        // "my folder/my document.pdf": combined spaces in directory and filename
        var fileEl3 = doc.Descendants(MetsNs + "file")
            .Single(f => (string)f.Attribute("ID")! == "FILE_objects/my folder/my document.pdf");
        fileEl3.Attribute("ADMID")!.Value.Should().Be("ADM_objects/my folder/my document.pdf");
    }

