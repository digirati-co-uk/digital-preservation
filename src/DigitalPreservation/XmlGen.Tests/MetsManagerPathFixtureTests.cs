/// <summary>
/// Tests that load pre-generated fixture METS files from the Samples directory and
/// verify that MetsManager and MetsParser behave correctly when editing and parsing
/// METS files that already contain paths with spaces, ampersands, and Unicode
/// characters.
///
/// These tests exercise a different scenario than MetsManagerPathTests: instead of
/// generating a fresh METS in each test, they load a persisted METS that was
/// generated by the same code and committed to the repo. This validates that:
///
///   - Saved METS files with special-char IDs round-trip correctly through the
///     full write-to-disk/read-from-disk cycle.
///   - MetsManager can navigate the existing XML structure (via PHYS_* Div IDs)
///     to add, update, and delete entries whose paths contain special characters.
///   - MetsParser produces the correct WorkingFile properties for every entry.
///
/// Each test copies its fixture from Samples/ to Outputs/ before making any edits,
/// so the committed Samples files are never modified by test runs.
///
/// Fixture files in this class:
///   Samples/path-fixture-spaces.xml  — paths with spaces (single and multiple)
///   Samples/path-fixture-special.xml — paths with ampersand and Unicode characters
///
/// To regenerate the fixtures (e.g. after changing MetsManager's output format),
/// run the Generate_Path_Special_Chars_Fixtures test (Category=Manual), then copy
/// the outputs from bin/Debug/net8.0/Outputs/ to Samples/.
/// </summary>
public class MetsManagerPathFixtureTests
{
    private readonly MetsManager metsManager;
    private readonly MetsParser parser;

    private static readonly XNamespace MetsNs = "http://www.loc.gov/METS/";

    // Digests used when generating the fixtures (must match what is in the fixture files)
    private const string Digest1 = "eb634d64ce8e6be5195174ceaef9ac9e19c37119f3b31618630aa633ccdbf68f";
    private const string Digest2 = "4675c73e6fd66d2ea9a684ec79e4e6559bb4d44a35e8234794b0691472b0385d";
    private const string Digest3 = "315bb3bd2eb2da5ce8b848bb7f09803d8a48e64021c4b6fe074aed9cc591c154";

    // Digest used in update operations (different from fixture digests to prove the update took effect)
    private const string UpdateDigest = "801d4a031510adb61ae11412c1554fbaa769a6b4428225ad87a489f92889f105";

    public MetsManagerPathFixtureTests()
    {
        var serviceProvider = new ServiceCollection()
            .AddLogging()
            .BuildServiceProvider();

        var factory = serviceProvider.GetService<ILoggerFactory>();
        var parserLogger = factory!.CreateLogger<MetsParser>();
        var metsLoader = new FileSystemMetsLoader();
        parser = new MetsParser(metsLoader, parserLogger);
        var metsStorage = new FileSystemMetsStorage(parser);
        var premisManager = new PremisManager();
        var premisManagerExif = new PremisManagerExif();
        var premisEventManager = new PremisEventManagerVirus();
        var metadataManager = new MetadataManager(premisManager, premisManagerExif, premisEventManager);
        metsManager = new MetsManager(parser, metsStorage, metadataManager);
    }

