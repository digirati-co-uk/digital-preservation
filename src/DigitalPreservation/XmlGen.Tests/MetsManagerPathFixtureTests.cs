    /// <summary>
    /// Copies a fixture from Samples to Outputs so the test can modify it
    /// without touching the committed file. Returns the URI to the copy.
    /// </summary>
    private static Uri CopyFixture(string fixtureName, string outputName)
    {
        var source = new FileInfo($"Samples/{fixtureName}");
        var dest = new FileInfo($"Outputs/{outputName}");
        File.Copy(source.FullName, dest.FullName, overwrite: true);
        return new Uri(dest.FullName);
    }

    /// <summary>
    /// Loads the ETag from the METS at the given URI (without passing an ETag to match,
    /// so it always succeeds regardless of current file content).
    /// </summary>
    private async Task<string> GetETag(Uri metsUri)
    {
        var result = await parser.GetMetsFileWrapper(metsUri);
        result.Success.Should().BeTrue();
        return result.Value!.ETag!;
    }

