    private async Task GenerateSpacesFixture(string outputPath)
    {
        var uri = new Uri(new FileInfo(outputPath).FullName);
        var createResult = await metsManager.CreateStandardMets(uri, "Spaces Test Fixture");
        createResult.Success.Should().BeTrue();
        var eTag = createResult.Value!.ETag!;

        // A file directly in objects/ with a single space in the name
        var file1 = new WorkingFile
        {
            LocalPath = "objects/my file.pdf",
            Name = "my file.pdf",
            ContentType = "application/pdf",
            Digest = Digest1,
            Size = 54321,
            Modified = new DateTime(2026, 1, 10, 9, 0, 0, DateTimeKind.Utc)
        };
        eTag = await DoAddFile(uri, file1, eTag);

        // A file with multiple spaces in the name
        var file2 = new WorkingFile
        {
            LocalPath = "objects/my great file.pdf",
            Name = "my great file.pdf",
            ContentType = "application/pdf",
            Digest = Digest2,
            Size = 12345,
            Modified = new DateTime(2026, 1, 11, 9, 0, 0, DateTimeKind.Utc)
        };
        eTag = await DoAddFile(uri, file2, eTag);

        // A subdirectory with a space in the name
        var dir = new WorkingDirectory
        {
            LocalPath = "objects/my folder",
            Name = "my folder",
            Modified = new DateTime(2026, 1, 12, 9, 0, 0, DateTimeKind.Utc)
        };
        eTag = await DoAddDirectory(uri, dir, eTag);

        // A file inside the spaced directory, itself with a space in the name
        var file3 = new WorkingFile
        {
            LocalPath = "objects/my folder/my document.pdf",
            Name = "my document.pdf",
            ContentType = "application/pdf",
            Digest = Digest3,
            Size = 99999,
            Modified = new DateTime(2026, 1, 13, 9, 0, 0, DateTimeKind.Utc)
        };
        await DoAddFile(uri, file3, eTag);
    }

